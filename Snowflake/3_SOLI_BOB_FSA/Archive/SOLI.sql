INSERT OVERWRITE INTO DEV.BUSINESS_OPERATIONS.SOLI (
  ORDER_NUMBER, 
  UNIQUE_KEY, 
  NS_LINE_NUMBER, 
  ITEM_NO, 
  ITEM_ID, 
  RECEIPT_DATE, 
  FREDD, 
  FK_SPA_ID, 
  SOURCE_LOAD_DATE
)
SELECT 
	ORDER_NUMBER,
    UNIQUE_KEY, 
    NS_LINE_NUMBER, 
    ITEM,
    ITEM_ID,
    AVAILABILITY_DATE,
    CAL.MIN_ADD_12::DATE AS "FREDD", 
    ID, 
    SOURCE_LOAD_DATE
FROM DEV.BUSINESS_OPERATIONS.SEQUENCING_PO_ASSIGN SPA
LEFT JOIN DEV.BUSINESS_OPERATIONS.DIM_FULFILLMENT_CALENDAR CAL
ON CAL.RAW_DATE = SPA.AVAILABILITY_DATE
ORDER BY ORDER_NUMBER, ITEM, UNIQUE_KEY